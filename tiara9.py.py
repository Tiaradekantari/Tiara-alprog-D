# suhu dalam fahrenheit = 89.6
# suhu dalam kelvin = 305.15
# suhu dalam reamur = 25.6

# suhu dalam celcius
celcius = 32

# rumus koversi suhu
farenheit = (celcius * 9/5) + 32
kelvin = celcius + 273.15
reamur = celcius * 4/5

# cetak nilai suhu
print("suhu dalam fahrenheit:", farenheit )
print("suhu dalam kelvin:", kelvin)
print("suhu dalam reamur:", reamur)